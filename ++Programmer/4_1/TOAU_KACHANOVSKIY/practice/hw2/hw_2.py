import numpy as np

# Данные
A = np.array([[0.2, 0.3, 0], [0.3, 0.3, 0.1], [0.2, 0.3, 0.2]])

Y = np.array([140, 420, 390])

k = np.array([1, 0.7, 0.7])

# Единичная матрица
I = np.eye(3)

# Валовый выпуск: X = (I - A)^-1 * Y
X = np.linalg.inv(I - A).dot(Y)

# Трудовые ресурсы: L_i = k_i * X_i
L = k * X

print("Валовый выпуск: ", X)

print("Трудовые ресурсы: ", L)

print("Общая потребность предприятия в трудовых ресурсах:", np.sum(L))
